import java.util.*;
public class evenorodd {
    public static void main(String args[]){
        Scanner vi=new Scanner(System.in);
        System.out.println("enter any number");
        int a=vi.nextInt();
        if(a%2==0){
            System.out.println("Its even number");
        }
        else{
            System.out.println("its odd number");
        }
   }
    
}
